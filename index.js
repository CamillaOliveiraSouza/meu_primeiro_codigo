function operacoes_basicas(x, y){

  let soma = x + y
  let subtracao = x - y
  let multiplicacao = x * y 
  let divisao = x / y 

  console.log(`O valor da soma de ${x} e ${y} é: ${soma}`)
  console.log(`O valor da subtracao de ${x} e ${y} é: ${subtracao}`)
  console.log(`O valor da multiplicacao de ${x} e ${y} é: ${multiplicacao}`)
  console.log(`O valor da divisao de ${x} e ${y} é: ${divisao}`)
  console.log("")
}

operacoes_basicas(1,0)
operacoes_basicas(11, 10)